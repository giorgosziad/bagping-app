  /* Merge into the global dictionary. Keys defined here OVERRIDE existing
     ones (radar_sub carries the new approved copy), everything else in
     BAGPING_I18N is left untouched. If BAGPING_I18N is missing entirely
     (i18n.js failed to load), create it so t() still resolves English. */
  if (typeof window.BAGPING_I18N === 'undefined') window.BAGPING_I18N = {};
  var dict = window.BAGPING_I18N;
  for (var lang in ADD) {
    if (!Object.prototype.hasOwnProperty.call(ADD, lang)) continue;
    if (!dict[lang]) dict[lang] = {};
    var pack = ADD[lang];
    for (var key in pack) {
      if (Object.prototype.hasOwnProperty.call(pack, key)) dict[lang][key] = pack[key];
    }
  }
